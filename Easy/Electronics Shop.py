def getMoneySpent(keyboards, drives, b):
    #
    # Write your code here.
    #
    list = []
    for x in keyboards:
        for y in drives:
            list.append(x+y)
    
    list.sort()

    if list[0] > b:
        return -1
    
    for x in range(2, len(list)):
        if list[x] > b:
            return list[x-1]
            

